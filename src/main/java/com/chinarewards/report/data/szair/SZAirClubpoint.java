package com.chinarewards.report.data.szair;

import java.util.Date;

public class SZAirClubpoint {

	private String partnerMembercardno;

	private Date transDate;

	private String shopName;

	private String status;

	public String getPartnerMembercardno() {
		return partnerMembercardno;
	}

	public void setPartnerMembercardno(String partnerMembercardno) {
		this.partnerMembercardno = partnerMembercardno;
	}

	public Date getTransDate() {
		return transDate;
	}

	public void setTransDate(Date transDate) {
		this.transDate = transDate;
	}

	public String getShopName() {
		return shopName;
	}

	public void setShopName(String shopName) {
		this.shopName = shopName;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
